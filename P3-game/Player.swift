import Foundation

class Player {
    
    var name = String()
    
    // ‚¨áÔ∏é Tableau de characters, correspondants aux 3 personnages de chaque joueur choisis en d√©but de partie
    var squad: [Character] = []
    
    // ‚¨áÔ∏é Tableau squad, mais avec √©l√©ments filtr√©s pour ne contenir que les characters encore vivants
    var aliveSquadCharacters: [Character] {
        squad.filter { $0.hp > 0 }
    }
    
    // ‚¨áÔ∏é Permet de savoir si tous les membres d'un squad sont morts, et donc si la partie est termin√©e
    var squadIsDead: Bool {
        if aliveSquadCharacters.isEmpty {
            return true
        } else {
            return false
        }
    }
    
    // ‚¨áÔ∏é Propri√©t√© permettant de garder une r√©f√©rence au character choisi par le joueur pour le round en cours (dans le contexte actuel, donne la possibilit√© de jongler avec les valeurs des armes et de leur redonner leurs valeurs par d√©faut au besoin)
    private var fightingCharacter = Character(name: "")
    
    // ‚¨áÔ∏é La valeur de chestChances d√©terminera si un coffre appara√Æt devant le personnage, ou non.
    private var chestChances = Int()
    
    // ‚¨áÔ∏é Cr√©ation des escouades de 3 personnages par le joueur. Dans la limite de 3, ajout des instances Character, choix du type de personnage puis d'un nom unique par l'utilisateur.
    func createMySquad() {
        let playableCharacters = [Warrior(), Magus(), Dwarf(), Colossus(), Priest()]
        while squad.count < 3 {
            print("üîî Choisis le personnage numero \(squad.count+1) parmi les suivants :\n")
            for characters in playableCharacters {
                print("\(characters.description)")
            }
            let choice = readLine()
            switch choice {
                /* Chaque cas permet dans l'ordre :
                 ‚Ä¢ d'ajouter un Character au tableau [squad]
                 ‚Ä¢ d'appeler la fonction chooseName dont le param√®tre correspond au type (Guerrier, Mage, Nain...) respectif de chaque personnage
                 */
            case "1" :
                squad.append(Warrior())
                chooseName(of: "Guerrier üë®üèø‚Äç‚öñÔ∏è")
            case "2" :
                squad.append(Magus())
                chooseName(of: "Mage üßôüèº‚Äç‚ôÇÔ∏è")
            case "3" :
                squad.append(Dwarf())
                chooseName(of: "Nain üë®üèø‚Äçüöí")
            case "4" :
                squad.append(Colossus())
                chooseName(of: "Colosse üèãüèª")
            case "5" :
                squad.append(Priest())
                chooseName(of: "Pr√™tre üßñüèº‚Äç‚ôÇÔ∏è")
            default: print("üö£‚Äç‚ôÇÔ∏è Merci de taper un chiffre entre 1 et 5 pour choisir le personnage correspondant")
            }
        }
    }
    
    
    // ‚¨áÔ∏é Permet √† l'utilisateur de choisir un nom unique pour chacun de ses trois personnages.
    private func chooseName(of type: String) {
        print("\nTu as opt√© pour un \(type) choisis-lui un nom üè∑")
        
        if let userInput = readLine()?.trimmingCharacters(in: .whitespacesAndNewlines), !userInput.isEmpty { // Si userInput est √©gale √† readLine (trimm√©e), et si elle n'est pas vide :
            if Character.charactersNames.contains(userInput) { // On v√©rifie dans le tableau r√©capitulatif de tous les noms si ce dernier existe d√©j√†
                print("Ce nom est d√©j√† pris.")
                chooseName(of: type)
            } else {
                Character.charactersNames.append(userInput) // On ajoute la valeur au tableau r√©capitulatif de tous les noms
                squad[squad.count-1].name = userInput // On assigne √©galement cette valeur √† la propri√©t√© 'name' du personnage en question
                print("\nAdjug√© vendu üôå Ton \(type) se nommera \(userInput) !\n\n")
            }
        } else {
            print("Choisis un nom valide")
            chooseName(of: type)
        }
    }
    
    // ‚¨áÔ∏é Permet √† l'utilisateur de choisir le personnage avec lequel il souhaite effectuer une action pour le round en cours
    func pickFighter() {
        print("üé≤ √âquipe \(name), saisis un chiffre correspondant au personnage avec lequel tu souhaites combattre :\n")
        // ‚¨áÔ∏é Affiche une liste des personnages vivants et disponibles dans l'escouade.
        for (index, character) in aliveSquadCharacters.enumerated() {
            print("\(index+1). \(character.name) le \(character.characterType) (\(character.hp)/\(character.maxHp) hp)\n")
        }
        if let choice = readLine() {
            switch choice {
            case "1" :
                choosenFighter(characterNumber: 0)
            case "2" :
                choosenFighter(characterNumber: 1)
            case "3" :
                choosenFighter(characterNumber: 2)
            default:
                print("\nüö£‚Äç‚ôÄÔ∏è Ce h√©ros n'existe pas. Tape un chiffre correspondant puis appuie sur 'Entr√©e'.")
                pickFighter()
            }
        }
        
    }
    
    private func choosenFighter(characterNumber: Int) {
        if squad.indices.contains(characterNumber) { // ‚Ä£ Permet de s√©curiser la saisie, dans le cas ou l'utilisateur souhaiterait appeler un personnage d√©j√† √©limin√©.
            fightingCharacter = aliveSquadCharacters[characterNumber] // ‚Ä£ On m√©morise quel personnage va au combat pour ce round
            print("\nTu as choisi de jouer avec \(fightingCharacter.name), ton \(fightingCharacter.characterType)")
            chest() // ‚Ä£ √©v√®nement al√©atoire
        } else {
            print("\nüö£‚Äç‚ôÄÔ∏è Choisis un personnage qui est encore vivant !")
            pickFighter()
        }
    }
    
    private func chest() {
        chestChances = Int.random(in: 1...10) // ‚Ä£ chances de voir un coffre appara√Ætre
        if chestChances <= 5 {
            fightingCharacter.chestWeapon = fightingCharacter.chestWeapons.randomElement()!
            print("\nAttends voir... üßù‚Äç‚ôÇÔ∏è‚ú®üßù Les elfes t'ont fait parvenir un coffre ! Voyons ce qu'il y a dedans... üîç")
            print("\nIl contient une arme : ‚ú® \(fightingCharacter.chestWeapon.weaponType) ‚ú® Cette arme inflige \(fightingCharacter.chestWeapon.damages) points de d√©g√¢ts !")
            keepChestWeaponOrNot(chestWeapon: fightingCharacter.chestWeapon)// ‚Ä£ Demande au joueur s'il souhaite garder l'arme trouv√©e
        }
    }
    
    // ‚¨áÔ∏é M√©thode de confirmation
    // FIXME: rentrer param√®tre defaultWeapon, et donc changer Weapon avec des let car √ßa ne changera pas 
    private func keepChestWeaponOrNot(chestWeapon: Weapon) {
        print("\nVeux-tu t'en √©quiper pour ce tour ?\n\n"
            + "1. Oui üôã\n"
            + "2. Non üôÖ")
        if let choice = readLine() {
            switch choice {
            case "1" :
                // ‚Ä£ Oui: L'arme actuelle est remplac√©e
                fightingCharacter.weapon = chestWeapon
                print("\nTr√®s bien, on la prend ! üéí\n")
            case "2" :
                fightingCharacter.weapon.damages = fightingCharacter.defaultCharacterDamages // ‚Ä£ Non: On r√©attribue les caract√©ristiques par d√©faut
                fightingCharacter.weapon.weaponType = fightingCharacter.defaultWeaponType
                print("\nTr√®s bien, on leur retourne le cadeau ! üí®")
            default :
                print("üö£‚Äç‚ôÇÔ∏è Merci de saisir un chiffre correspondant √† Oui ou Non.")
                keepChestWeaponOrNot(chestWeapon: chestWeapon)
            }
        }
    }
    
    func chooseFighterAction(characters: [Character]) {
        
        print("üîî Que veux-tu faire ?\n"
            + "\n1. Soigner un alli√© ‚õë"
            + "\n2. Attaquer un membre de l'escouade adverse üî™")
        if let choice = readLine() {
            switch choice {
            case "1" :
                healAlly(characters: characters)

            case "2" :
                attackEnnemy(characters: characters)
                
            default: print("üö£‚Äç‚ôÄÔ∏è Merci de taper un chiffre correspondant √† l'une des deux options.")
            chooseFighterAction(characters: characters)
            }
        }
    }
    
    private func healAlly(characters: [Character]) { // Le param√®tre est utilis√© pour le case 0, dans le cas ou le joueur revient au menu pr√©c√©dent.
        
        print("Quel alli√© veux-tu soigner ? üè•\n")
        for (index, character) in aliveSquadCharacters.enumerated() {
            print("\(index+1). Soigner \(character.name) ton \(character.characterType) (\(character.hp)/\(character.maxHp) hp) \n")
        }
        print("0. Effectuer une autre action üôáüèª‚Äç‚ôÇÔ∏è")
        
        if let choice = readLine() {
            switch choice {
            case "1" :
                heal(characterNumber: 0)
            case "2" :
                heal(characterNumber: 1)
            case "3" :
                heal(characterNumber: 2)
            case "0" :
                chooseFighterAction(characters: characters) // ‚Ä£ Le joueur peut revenir au menu pr√©c√©dent s'il le souhaite.
                
            default :
                print("\nüö£‚Äç‚ôÇÔ∏è Merci de saisir un chiffre correspondant √† l'un des personnages de ton escouade, ou taper '0' puis 'Entr√©e' pour effectuer une autre action.\n\n")
                healAlly(characters: aliveSquadCharacters)
            }
            
        }
    }
    
    private func heal(characterNumber: Int) { // ‚Ä£ "Soigner caract√®re num√©ro: 0, 1 ou 2"
        let target = squad[characterNumber]
        if squad.indices.contains(characterNumber) {
            if target.hp <= target.maxHp - fightingCharacter.healSkill { // ‚Ä£ Si les HP actuels du character cibl√© par le soin ont un √©cart sup√©rieur ou √©gal au montant de la propri√©t√© HealSkill, compar√© √† son maxHP, ajouter ce montant en entier.
                target.hp += fightingCharacter.healSkill
                print("\(target.name) r√©cup√®re \(fightingCharacter.healSkill) points de vie ‚ô•Ô∏è \(target.name) a d√©sormais \(target.hp) hp\n")
            } else if target.hp == target.maxHp { // ‚Ä£ Si les hp actuels du character sont d√©j√† au maximum :
                print("\nüö£‚Äç‚ôÇÔ∏è Ce personnage a d√©j√† le maximum de points de vie. Soigne un autre membre de ton escouade ou effectue une autre action.\n\n")
                healAlly(characters: aliveSquadCharacters)
            } else { // ‚Ä£ Si les HP actuels du character ont un √©cart inf√©rieur au montant de la comp√©tence healskill, ajouter seulement la diff√©rence pour atteindre le maxHp.
                print("\(target.name) r√©cup√®re \(target.maxHp - target.hp) points de vie ‚ô•Ô∏è")
                target.hp += target.maxHp - target.hp
                print(" \(target.name) a d√©sormais \(target.maxHp) hp\n")
            }
        } else {
            print("üö£‚Äç‚ôÇÔ∏è Bien essay√© ! S√©l√©ctionne un h√©ros vivant.")
            healAlly(characters: aliveSquadCharacters)
        }
        
    }
    
    
    
    private func attackEnnemy(characters: [Character]) {
        print("\n\nQuel ennemi veux-tu attaquer ? ‚öîÔ∏è\n")
        for (index, character) in characters.enumerated() {
            print("\(index+1). Attaquer \(character.name) le \(character.characterType) (\(character.hp)/\(character.maxHp) hp)\n")
        }
        print("0. Effectuer une autre action üôáüèª‚Äç‚ôÇÔ∏è")
        
        if let choice = readLine() {
            switch choice {
            case "1" : attack(character: characters[0]) // "Attaquer le character num√©ro: "
            case "2" : attack(character: characters[1])
            case "3" : attack(character: characters[2])
            case "0" : chooseFighterAction(characters: characters) // ‚Ä£ Le joueur peut revenir au menu pr√©c√©dent s'il le souhaite.
            default: print("üö£‚Äç‚ôÇÔ∏è Merci de saisir un chiffre correspondant √† l'action souhait√©e\n")
            attackEnnemy(characters: characters)
            }
            
        }
    }
    

    private func attack(character: Character) {
        
        
        character.hp -= fightingCharacter.weapon.damages
        print("\nTon h√©ros frappe \(character.name) pour \(fightingCharacter.weapon.damages) de d√©g√¢ts ! üíî\n")
        if character.hp > 0 {
            print("\(character.name) a d√©sormais \(character.hp)/\(character.maxHp) hp\n\n")
        } else {
            print("\(character.name) n'a plus aucun point de vie üíÄ \(character.name) est retir√© de l'escouade !\n\n\n")
            character.hp = 0
        }
        fightingCharacter.weapon.damages = fightingCharacter.defaultCharacterDamages
    }
}
